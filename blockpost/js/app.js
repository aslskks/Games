function normalizeUrl(url) {
    if (!url) return '';
    if (/^https?:\/\//.test(url)) return url;
    return 'https://' + url;
}

function normalizeDomain(domain) {
    return domain.replace(/^https?:\/\//, '').replace(/\/+$/, '');
}

function dedupe(items) {
    return [...new Set(items.filter(Boolean))];
}

function findGame(gameId) {
    return CONFIG.games.find(g => g.id === gameId) || null;
}

function getResolvedEmbedHosts(gameId) {
    const game = findGame(gameId);
    return dedupe([
        ...CONFIG.globalEmbedHosts,
        ...((game && game.embedHosts) || [])
    ]);
}

function getResolvedAllowedEmbedDomains(gameId) {
    const game = findGame(gameId);
    return dedupe([
        ...CONFIG.globalAllowedEmbedDomains,
        ...((game && game.allowedEmbedDomains) || [])
    ]);
}

function getGameSpecificAllowedEmbedDomains(gameId) {
    const game = findGame(gameId);
    return dedupe(((game && game.allowedEmbedDomains) || []).map(normalizeDomain));
}

function getResolvedPlayUrls(gameId) {
    const game = findGame(gameId);
    const gamePlayUrls = (game && game.playUrls) || [];

    if (gamePlayUrls.length > 0) {
        return dedupe(gamePlayUrls.map(normalizeUrl));
    }

    return CONFIG.globalPlayBaseUrls.map(normalizeUrl);
}

function getAllBaseUrls() {
    return CONFIG.globalEmbedHosts;
}

function getGameAllowedDomains(gameId) {
    return getResolvedAllowedEmbedDomains(gameId).map(normalizeDomain);
}

function getAllGameUrls(gameId) {
    return getResolvedEmbedHosts(gameId).map(baseUrl => ({
        url: `${baseUrl}/${gameId}/`,
        baseUrl: baseUrl
    }));
}

function getGamesList() {
    return CONFIG.games.sort((a, b) => a.name.localeCompare(b.name));
}

function isDomainAllowed() {
    const currentDomain = window.location.hostname;
    const gameId = getCurrentGameId();

    return getResolvedAllowedEmbedDomains(gameId).some(domain => {
        const cleanDomain = normalizeDomain(domain);

        return currentDomain === cleanDomain || currentDomain.endsWith('.' + cleanDomain);
    });
}

function isEmbedded() {
    try {
        return window.parent !== window;
    } catch (e) {
        return true;
    }
}

function isDirectAccess() {
    return !isEmbedded();
}

function clearGameContent() {
    const body = document.body;
    const modals = document.querySelectorAll('#embed-protection-modal');

    body.innerHTML = '';

    modals.forEach(modal => {
        body.appendChild(modal);
    });

    body.style.margin = '0';
    body.style.padding = '0';
    body.style.overflow = 'hidden';
    body.style.backgroundColor = '#000';
}

function preventDevTools() {
    document.addEventListener('contextmenu', e => e.preventDefault());

    document.addEventListener('keydown', e => {
        if (e.key === 'F12' ||
            (e.ctrlKey && e.shiftKey && (e.key === 'I' || e.key === 'J')) ||
            (e.ctrlKey && e.key === 'U')) {
            e.preventDefault();
            return false;
        }
    });

    document.addEventListener('selectstart', e => e.preventDefault());
    document.addEventListener('dragstart', e => e.preventDefault());
}

function getCurrentGameId() {
    const path = window.location.pathname;

    const segments = path.split('/').filter(segment => segment);

    if (segments.length > 0) {
        return segments[0];
    }

    return null;
}

function getAllowedDomainsForCurrentGame() {
    const gameId = getCurrentGameId();
    return getResolvedAllowedEmbedDomains(gameId).map(normalizeDomain);
}

function getPlayUrlsForCurrentGame() {
    const gameId = getCurrentGameId();
    return getResolvedPlayUrls(gameId);
}

function getDomainLabel(url) {
    return new URL(url).hostname.replace(/^www\./, '');
}

function isPagesDevAccess() {
    const hostname = window.location.hostname;
    return hostname === 'pages.dev' || hostname.endsWith('.pages.dev');
}

function getPreferredPublicUrl() {
    const gameId = getCurrentGameId();
    const playUrls = getResolvedPlayUrls(gameId);
    return playUrls[0] || normalizeUrl(CONFIG.globalPlayBaseUrls[0]);
}

function redirectPagesDevAccess() {
    if (!isPagesDevAccess()) {
        return false;
    }

    const targetUrl = getPreferredPublicUrl();
    if (!targetUrl) {
        return false;
    }

    window.location.replace(targetUrl);
    return true;
}

function showProtectionModal() {
    console.error('Blocking protection modal shown - possible unauthorized embedding detected');
}

function initEmbedProtection() {
    const pathname = window.location.pathname;
    const protectionDisabled = document.body && document.body.dataset.disableEmbedProtection === 'true';

    if (protectionDisabled) {
        return;
    }

    if (pathname === '/' || pathname === '/index.html' || pathname === '') {
        return;
    }

    const shouldBlockForDomain = !isDomainAllowed();
    const shouldBlockForDirectAccess = isDirectAccess() && !CONFIG.settings.allowDirectAccess;

    if (shouldBlockForDomain || shouldBlockForDirectAccess) {
        setTimeout(() => {
            showProtectionModal();
        }, CONFIG.settings.disableDelay);
    }
}

function startProtection() {
    setTimeout(initEmbedProtection, 100);
}

if (!redirectPagesDevAccess()) {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', startProtection);
    } else {
        startProtection();
    }
}

window.copyToClipboard = function (text, button) {
    if (navigator.clipboard && window.isSecureContext) {
        navigator.clipboard.writeText(text).then(() => {
            showCopyFeedback(button);
        }).catch(err => {
            console.error('Clipboard API failed: ', err);
            fallbackCopy(text, button);
        });
    } else {
        fallbackCopy(text, button);
    }
}

function fallbackCopy(text, button) {
    try {
        const textArea = document.createElement('textarea');
        textArea.value = text;
        textArea.style.position = 'fixed';
        textArea.style.left = '-999999px';
        textArea.style.top = '-999999px';
        document.body.appendChild(textArea);
        textArea.focus();
        textArea.select();

        const successful = document.execCommand('copy');
        document.body.removeChild(textArea);

        if (successful) {
            showCopyFeedback(button);
        } else {
            showCopyError(button);
        }
    } catch (err) {
        console.error('Fallback copy failed: ', err);
        showCopyError(button);
    }
}

function showCopyFeedback(button) {
    const originalText = button.textContent;
    button.textContent = 'Copied!';
    button.classList.add('copied');

    setTimeout(() => {
        button.textContent = originalText;
        button.classList.remove('copied');
    }, 2000);
}

function showCopyError(button) {
    const originalText = button.textContent;
    button.textContent = 'Failed';
    button.style.backgroundColor = '#ef4444';

    setTimeout(() => {
        button.textContent = originalText;
        button.style.backgroundColor = '';
    }, 2000);
}

function displayConfig() {
    const baseUrlsDiv = document.getElementById('base-urls');
    const allowedDomainsDiv = document.getElementById('allowed-domains');
    const playUrlsDiv = document.getElementById('play-urls');

    if (!baseUrlsDiv || !allowedDomainsDiv || !playUrlsDiv) return;

    CONFIG.globalEmbedHosts.forEach((url, index) => {
        const urlDiv = document.createElement('div');
        urlDiv.className = 'flex items-center space-x-2';
        urlDiv.innerHTML = `
            <span class="text-sm text-gray-600">${index + 1}.</span>
            <code class="bg-gray-100 px-2 py-1 rounded text-sm">${url}</code>
        `;
        baseUrlsDiv.appendChild(urlDiv);
    });

    CONFIG.globalAllowedEmbedDomains.forEach((domain, index) => {
        const domainDiv = document.createElement('div');
        domainDiv.className = 'flex items-center space-x-2';
        domainDiv.innerHTML = `
            <span class="text-sm text-gray-600">${index + 1}.</span>
            <span class="text-sm text-gray-800">${domain}</span>
        `;
        allowedDomainsDiv.appendChild(domainDiv);
    });

    CONFIG.globalPlayBaseUrls.forEach((url, index) => {
        const urlDiv = document.createElement('div');
        urlDiv.className = 'flex items-center space-x-2';
        urlDiv.innerHTML = `
            <span class="text-sm text-gray-600">${index + 1}.</span>
            <code class="bg-gray-100 px-2 py-1 rounded text-sm">${url}</code>
        `;
        playUrlsDiv.appendChild(urlDiv);
    });
}

function displayOverview() {
    const totalGamesEl = document.getElementById('hero-total-games');
    const globalHostsEl = document.getElementById('hero-global-hosts');
    const overrideGamesEl = document.getElementById('hero-override-games');

    if (!totalGamesEl || !globalHostsEl || !overrideGamesEl) return;

    const overrideGamesCount = CONFIG.games.filter(game => getGameSpecificAllowedEmbedDomains(game.id).length > 0).length;

    totalGamesEl.textContent = String(CONFIG.games.length);
    globalHostsEl.textContent = String(CONFIG.globalEmbedHosts.length);
    overrideGamesEl.textContent = String(overrideGamesCount);
}

function displayEmbedAudit() {
    const auditDiv = document.getElementById('embed-audit');
    const auditCount = document.getElementById('embed-audit-count');

    if (!auditDiv || !auditCount) return;

    const gamesWithSpecificDomains = getGamesList()
        .map(game => ({
            game,
            domains: getGameSpecificAllowedEmbedDomains(game.id)
        }))
        .filter(entry => entry.domains.length > 0);

    auditCount.textContent = `${gamesWithSpecificDomains.length} game${gamesWithSpecificDomains.length === 1 ? '' : 's'} with overrides`;

    if (gamesWithSpecificDomains.length === 0) {
        auditDiv.innerHTML = '<p class="text-sm text-gray-500">No game-specific embed domain overrides configured.</p>';
        return;
    }

    gamesWithSpecificDomains.forEach(entry => {
        const item = document.createElement('div');
        item.className = 'w-full lg:w-auto lg:min-w-[280px] rounded-xl border border-blue-100 bg-blue-50/70 px-4 py-3';
        item.innerHTML = `
            <div class="text-sm font-semibold text-gray-800 mb-2">${entry.game.name}</div>
            <div class="flex flex-wrap gap-2">
                ${entry.domains.map(domain => `
                    <span class="audit-chip">${domain}</span>
                `).join('')}
            </div>
        `;
        auditDiv.appendChild(item);
    });
}

