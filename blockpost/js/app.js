function filterGames() {
    const searchInput = document.getElementById('game-search');
    const resultsCount = document.getElementById('games-count');
    const gamesListDiv = document.getElementById('games-list');
    const noResults = document.getElementById('no-results');

    if (!searchInput || !resultsCount || !gamesListDiv || !noResults) return;

    const query = searchInput.value.trim().toLowerCase();
    const cards = Array.from(gamesListDiv.querySelectorAll('.game-card'));
    let visibleCount = 0;

    cards.forEach(card => {
        const searchableText = (card.dataset.search || '').toLowerCase();
        const isVisible = query === '' || searchableText.includes(query);

        card.style.display = isVisible ? '' : 'none';
        if (isVisible) {
            visibleCount += 1;
        }
    });

    resultsCount.textContent = `${visibleCount} game${visibleCount === 1 ? '' : 's'}`;
    noResults.classList.toggle('hidden', visibleCount !== 0);
}

if (!isPagesDevAccess()) {
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', function () {
            displayOverview();
            displayConfig();
            displayEmbedAudit();
            displayGames();
            filterGames();
            const searchInput = document.getElementById('game-search');
            if (searchInput) {
                searchInput.addEventListener('input', filterGames);
            }
        });
    } else {
        displayOverview();
        displayConfig();
        displayEmbedAudit();
        displayGames();
        filterGames();
        const searchInput = document.getElementById('game-search');
        if (searchInput) {
            searchInput.addEventListener('input', filterGames);
        }
    }
}

